public class AtomDefinition{

    public String Name;
    public String Type1, Type2, Type3;
    public double q1,q2,q3,p1,p2,p3;

    public AtomDefinition(String inputName, String type1, String type2, String type3){
	Name=inputName;
	Type1=type1;
	Type2=type2;
	Type3=type3;
	q1=0.0;
	q2=0.0;
	q3=0.0;
	p1=0.0;
	p2=0.0;
	p3=0.0;
    }

}
