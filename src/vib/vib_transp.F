      SUBROUTINE vib_TRANSP(A,NDIM,N)
C*MODULE VIB  *DECK TRANSP
* $Id: vib_transp.F,v 1.1 1995-07-29 23:19:45 d3e129 Exp $
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      DIMENSION A(NDIM,NDIM)
C
C  TRANSPOSE MATRIX A, STORE RESULT IN A.
C
      DO 10 I=1,N
        DO 10 J=1,I
          TEMP=A(I,J)
          A(I,J)=A(J,I)
   10     A(J,I)=TEMP
      RETURN
      END
